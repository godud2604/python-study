# variable

# str
name = 'Bro'
print(name)
print(type(name)) # <class 'str'>

# int
age = 21
age += 1
print(age)
print(type(age)) # <class 'int'>
print("Your are is : " + str(age)) # Your are is : 22

# float = floating point number (a decimal number)
height = 250.5
print(type(height)) # <class 'float'>

# boolean 
human = False
print(human)